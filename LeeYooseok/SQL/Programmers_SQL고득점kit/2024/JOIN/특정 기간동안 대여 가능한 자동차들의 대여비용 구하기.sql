SELECT C.CAR_ID, C.CAR_TYPE, ROUND(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100, 0) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P ON C.CAR_TYPE = P.CAR_TYPE AND DURATION_TYPE LIKE('30일%')
WHERE C.CAR_TYPE IN ('세단', 'SUV') AND
    CAR_ID NOT IN (SELECT CAR_ID
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE START_DATE BETWEEN '2022-11-01' AND '2022-12-01'
    OR END_DATE BETWEEN '2022-11-01' AND '2022-12-01')AND
    ROUND(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100, 0) >= 500000 AND
    ROUND(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100, 0) < 2000000
ORDER BY FEE DESC, CAR_TYPE, CAR_ID DESC